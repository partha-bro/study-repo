# To the power
print(2*2*2) # 8
print(2**3) # Exponent: 2 to the power 3 = 8
print(19/9) # 2.1111111111
print(round(19/9,4)) # 2.1111 

print(2 * 3 + 5) # left to right [2*3+5] [6+5] [11]
print(5 ** 3 ** 2) # right to left [5**3**2] [5**9] [1953125]
